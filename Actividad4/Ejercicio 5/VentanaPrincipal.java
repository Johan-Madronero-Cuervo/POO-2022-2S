    private void EliminarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarItemActionPerformed
        int indice = L.getSelectedIndex();
        if (indice >= 0) { 
            listModel.removeElementAt(indice);
            lista.eliminarPersona(indice);
        }else{JOptionPane.showMessageDialog(null, "Debe seleccionar un elemento");
        }
    }//GEN-LAST:event_EliminarItemActionPerformed

    private void BorrarLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BorrarLActionPerformed
        listModel.removeAllElements();
        lista.borrarLista();
        JOptionPane.showMessageDialog(null, "La lista fue vaciada exitosamente");
    }//GEN-LAST:event_BorrarLActionPerformed

