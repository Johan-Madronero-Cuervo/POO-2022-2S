    private void AñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AñadirActionPerformed
        Persona PersonaX = new Persona(NameField.getText(),
        AField.getText(),
        TelField.getText(), DirField.getText());
        lista.añadirPersona(PersonaX); 
        String elemento = PersonaX.nombre + " " +
        PersonaX.apellidos +
        " " + PersonaX.telefono + " " + PersonaX.direccion;
        /* Se limpian los textfields */
        listModel.addElement(elemento);
        NameField.setText(" ");
        AField.setText(" ");
        TelField.setText(" ");
        DirField.setText(" "); 
    }//GEN-LAST:event_AñadirActionPerformed

